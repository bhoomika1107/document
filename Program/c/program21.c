//>, <, >=, <=, ==, !=
#include<stdio.h>
int main()
{
    int a=5,b=5;
    int res;
    res=a>b;
    printf("%d\n",res);
    res=a<b;
    printf("%d\n",res);
    res=a>=b;
    printf("%d\n",res);
    res=a<=b;
    printf("%d\n",res);
    res=a==b;
    printf("%d\n",res);
    res=a!=b;
    printf("%d\n",res);
    return 0;
}